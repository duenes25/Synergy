function strategySelected() {
  var strategyDiv = document.getElementById("strategyDiv");
  var talentDiv = document.getElementById("talentDiv");
  var leadershipDiv = document.getElementById("leadershipDiv");
  var planningDiv = document.getElementById("planningDiv");
  strategyDiv.style.display = "block";
  talentDiv.style.display = "none";
  leadershipDiv.style.display = "none";
  planningDiv.style.display = "none";
}

